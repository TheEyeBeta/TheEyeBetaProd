def summarize_categories(entries: list[ControlMatrixEntry]) -> list[ControlMatrixCategorySummary]:
    """Aggregate entry counts per category."""
    buckets: dict[str, list[ControlMatrixEntry]] = {}
    for entry in entries:
        buckets.setdefault(entry.category, []).append(entry)

    summaries: list[ControlMatrixCategorySummary] = []
    for category in sorted(buckets.keys()):
        rows = buckets[category]
        summaries.append(
            ControlMatrixCategorySummary(
                category=category,
                total=len(rows),
                viewable=sum(1 for r in rows if r.viewable),
                controllable=sum(1 for r in rows if r.controllable),
                critical_count=sum(1 for r in rows if r.priority == "Critical"),
                gap_count=sum(
                    1 for r in rows if r.backend_gap or r.frontend_gap
                ),
            ),
        )
    return summaries


