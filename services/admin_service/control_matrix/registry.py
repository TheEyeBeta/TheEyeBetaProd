def _entry(
    *,
    id: str,
    title: str,
    category: str,
    role_required: str = "operator",
    viewable: bool = False,
    controllable: bool = False,
    editable: bool = False,
    schedulable: bool = False,
    kill_switch: bool = False,
    dangerous: bool = False,
    confirmation_required: bool = False,
    audit_required: bool = False,
    audit_implemented: bool = False,
    priority: str = "Medium",
    backend_source: str | None = None,
    frontend_location: str | None = None,
    api_route: str | None = None,
    backend_gap: str | None = None,
    frontend_gap: str | None = None,
    backend_only_reason: str | None = None,
    cloudflare_dependency: str | None = None,
    service_port_dependency: str | None = None,
    trusted_host_dependency: bool = False,
    health_endpoint: str | None = None,
    notes: str | None = None,
) -> ControlMatrixEntry:
    """Construct a matrix row with dangerous-action defaults applied."""
    if dangerous:
        confirmation_required = True
        audit_required = True
    return ControlMatrixEntry(
        id=id,
        title=title,
        category=category,
        backend_source=backend_source,
        frontend_location=frontend_location,
        api_route=api_route,
        role_required=role_required,
        viewable=viewable,
        controllable=controllable,
        editable=editable,
        schedulable=schedulable,
        kill_switch=kill_switch,
        dangerous=dangerous,
        confirmation_required=confirmation_required,
        audit_required=audit_required,
        audit_implemented=audit_implemented,
        backend_gap=backend_gap,
        frontend_gap=frontend_gap,
        backend_only_reason=backend_only_reason,
        cloudflare_dependency=cloudflare_dependency,
        service_port_dependency=service_port_dependency,
        trusted_host_dependency=trusted_host_dependency,
        health_endpoint=health_endpoint,
        priority=priority,
        notes=notes,
    )


