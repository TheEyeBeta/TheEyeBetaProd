    """Build the API response with categories and drift alerts."""
    from control_matrix.staleness import collect_staleness_alerts
    from control_matrix.validation import collect_drift_alerts, validate_matrix_invariants

    entries = build_control_matrix()
    violations = validate_matrix_invariants(entries)
    staleness = collect_staleness_alerts(entries)
    drift = collect_drift_alerts(entries)
    if violations:
        drift = [*drift, *[f"MATRIX INVARIANT: {v}" for v in violations]]
    if staleness:
        drift = [*drift, *staleness]

    return ControlMatrixResponse(
        version=MATRIX_VERSION,
        generated_at=datetime.now(tz=UTC),
        entries=entries,
        categories=summarize_categories(entries),
        drift_alerts=drift,
    )
